package cn.gtmap.variable;
/**
 * 表达式
 * @author Administrator
 *
 */
public class Expression {

	public static void main(String[] args) {
		/**
		 * 1、以;结尾的一段代码，即为一个表达式
		 */
		int i= 5;
		System.out.println(i);
		
		/**
		 * 2、; 也是一个完整的表达式
		 */
		//一个空;也是一个表达式
		;
		;
		;
		;
		;
	}

}
